    private void btncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariActionPerformed
        defaultTableModel.getDataVector().removeAllElements();
        defaultTableModel.fireTableDataChanged();
        String search = txtcari.getText().toString();
        connection = Connector.ConnectDb();
        try {
            flag=0;
            sql = "select Count,id_obat,Nama_Obat,Tipe_Obat,Harga_Obat from tbl_obatobatan where id_obat = '" + search + "'";

            ps = connection.prepareStatement(sql);
            rs = ps.executeQuery();
            Object columnData[] = new Object[14];
            while (rs.next()) {
                flag=1;
                columnData[0] = rs.getString("Count");
                columnData[1] = rs.getString("id_obat");
                columnData[2] = rs.getString("Nama_Obat");
                columnData[3] = rs.getString("Tipe_Obat");
                columnData[4] = rs.getString("Harga_Obat");

                defaultTableModel.addRow(columnData);
            }
            if (flag == 0) {
                DefaultTableModel dtm = (DefaultTableModel) tblObat.getModel();
                dtm.setRowCount(0);
                loadtable();
                JOptionPane.showMessageDialog(null, "ID Tidak Ditemukan", "Warning",
                        JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            System.out.println("Error " + e.getMessage());
        }
    }//GEN-LAST:event_btncariActionPerformed

