    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        int ops = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Mengubah Data Ini?", "Information", JOptionPane.YES_NO_OPTION);
        if (ops == JOptionPane.NO_OPTION) {

            DefaultTableModel dtm = (DefaultTableModel) tblObat.getModel();
            dtm.setRowCount(0);
            loadtable();

        }
        if (ops == JOptionPane.YES_OPTION) {
            if (txtnamaobat.getText().equals("")
                    || cbtipeobat.getSelectedItem().toString().equals("Pilih Tipe Obat")
                    || txtharga.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Form Pengisian Tidak Boleh Kosong", "Warning",
                        JOptionPane.WARNING_MESSAGE);

            } else {

                getfield();
                connection = Connector.ConnectDb();
                try {
                    sql = "UPDATE tbl_obatobatan SET Nama_Obat = ?, Tipe_Obat = ?,Harga_Obat = ? WHERE Id_obat = ?";
                    ps = connection.prepareStatement(sql);

                    ps.setString(1, uName);
                    ps.setString(2, tipeobat);
                    ps.setInt(3, harga);
                    ps.setString(4, uId);

                    int rs = ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data Sudah Terperbaharui");
                    DefaultTableModel dtm = (DefaultTableModel) tblObat.getModel();
                    dtm.setRowCount(0);
                    loadtable();
                } catch (Exception e) {
                    System.out.println("Error " + e.getMessage());
                }
            }
        } else {
            clear();
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void tblObatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblObatMouseClicked

        int i = tblObat.getSelectedRow();
        TableModel model = tblObat.getModel();

        txtidobat.setText(model.getValueAt(i, 1).toString());
        txtnamaobat.setText(model.getValueAt(i, 2).toString());
        txtharga.setText(model.getValueAt(i, 4).toString());
        String penyakit = model.getValueAt(i, 3).toString();
        switch (penyakit) {
            case "Pilih Tipe Obat":
                cbtipeobat.setSelectedIndex(0);
                break;
            case "Gigi":
                cbtipeobat.setSelectedIndex(1);
                break;
            case "THT":
                cbtipeobat.setSelectedIndex(2);
                break;
            case "Jantung":
                cbtipeobat.setSelectedIndex(3);
                break;
        }

    }//GEN-LAST:event_tblObatMouseClicked

