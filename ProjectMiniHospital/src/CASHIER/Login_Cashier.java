    private void loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginMouseClicked
        connection = Connector.ConnectDb();
        String user = txtusername.getText();
        String pass = String.valueOf(txtpassfield.getPassword());
        if (txtusername.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Masukkan Username dan Password", "Warning",
                    JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                String sql = "select username, password from tbl_kasir where username='" + user + "'";
                prp = connection.prepareStatement(sql);
                result = prp.executeQuery();
                result.first();
                if (pass.equals(result.getString("password"))) {
                    JOptionPane.showMessageDialog(null, "Login Berhasil", "Selamat Datang " + user, JOptionPane.INFORMATION_MESSAGE);
                    Cashier_Activity rActivity = new Cashier_Activity();
                    rActivity.setVisible(true);
                    username = user;
                    Cashier_Activity.username = username;
                    dispose();
                    connection.close();
                } else {
                    JOptionPane.showMessageDialog(null, "User atau Password Salah", "Error", JOptionPane.WARNING_MESSAGE);
                    txtusername.setText("");
                    txtpassfield.setText("");
                }

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "User atau Password Salah");
                txtusername.setText("");
                txtpassfield.setText("");
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_loginMouseClicked

